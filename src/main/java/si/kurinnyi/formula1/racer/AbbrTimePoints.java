package si.kurinnyi.formula1.racer;

public class AbbrTimePoints {
    private String abbr;
    private String time;

    public AbbrTimePoints(String abbr, String time) {
        this.abbr = abbr;
        this.time = time;
    }

    public String getAbbr() {
        return abbr;
    }

    public String getTime() {
        return time;
    }

}
